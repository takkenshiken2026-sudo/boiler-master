def _split_two_paragraphs(text: str) -> str:
    """term_detail_body を空行区切りの2段落に分ける（検証要件）。"""
    import re as _re

    if "\n\n" in text:
        return text
    sents = [s for s in _re.split(r"(?<=。)", text) if s.strip()]
    if len(sents) < 2:
        return text
    mid = (len(sents) + 1) // 2
    return "".join(sents[:mid]).strip() + "\n\n" + "".join(sents[mid:]).strip()


