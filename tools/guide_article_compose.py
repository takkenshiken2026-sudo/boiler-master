def pick(key: str, options: list[str]) -> str:
    if not options:
        return ""
    return options[hash(key) % len(options)]


def norm(value: str | None) -> str:
    return (value or "").strip()


@dataclass
class GuideContext:
    slug: str
    genre: str
    title: str
    topic: str
    intent: str
    field_short: str | None
    field_full: str | None
    tags: list[str]


def build_context(row: dict[str, str]) -> GuideContext:
    slug = norm(row.get("slug"))
    genre = norm(row.get("genre")) or "学習法"
    title = norm(row.get("title"))
    intent = norm(row.get("user_intent")) or title
    tags = [t.strip() for t in norm(row.get("tags")).split(";") if t.strip()]

    topic = title
    for prefix in (
        "2級ボイラー技士試験の",
        "2級ボイラー技士",
        "ボイラー",
    ):
        if topic.startswith(prefix):
            topic = topic[len(prefix) :].lstrip("の")
    topic = re.split(r"｜|\|", topic)[0].strip() or title

    field_short = None
    field_full = None
    slug_l = slug.lower()
    for key, short, full in FIELD_FROM_SLUG:
        if key in slug_l:
            field_short = short
            field_full = full
            break

    return GuideContext(
        slug=slug,
        genre=genre,
        title=title,
        topic=topic,
        intent=intent,
        field_short=field_short,
        field_full=field_full,
        tags=tags,
    )


