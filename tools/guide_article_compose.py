def _strip_trailing_periods(s: str) -> str:
    s = s.strip()
    while s.endswith("。") or s.endswith("．"):
        s = s[:-1].rstrip()
    return s


def compose_meta(ctx: GuideContext) -> str:
    base = _strip_trailing_periods(ctx.intent or ctx.topic)
    text = f"{exam_name()}の「{ctx.topic}」について、{base}。公式確認・演習・用語の進め方を整理。"
    return text if len(text) <= 155 else text[:154] + "…"


