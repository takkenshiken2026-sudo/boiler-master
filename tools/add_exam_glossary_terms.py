def main() -> int:
    if not GLOSSARY_CSV.is_file():
        print(f"Missing {GLOSSARY_CSV}", file=sys.stderr)
        return 1

    rows = list(csv.DictReader(GLOSSARY_CSV.read_text(encoding="utf-8-sig").splitlines()))
    if not rows:
        print("glossary_terms.csv is empty", file=sys.stderr)
        return 1

    fieldnames = list(rows[0].keys())
    existing = {r["term"].strip() for r in rows}
    added = 0

    for term, reading, category, short_def, definition, related, importance in NEW_TERMS:
        if term in existing:
            print(f"skip (exists): {term}")
            continue
        rows.append(
            {
                "term": term,
                "reading": reading,
                "category": category,
                "tags": "重要用語;頻出用語",
                "short_def": short_def,
                "definition": definition,
                "related_terms": related,
                "legal_basis": "",
                "importance": importance,
                "explanation": EXPLANATION.format(term=term),
            }
        )
        existing.add(term)
        added += 1
        print(f"add: {term}")

    if not added:
        print("No new terms to add.")
        return 0

    with GLOSSARY_CSV.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)

    print(f"Added {added} terms ({len(rows)} total).")
    print("Run: python3 tools/build_glossary_pages.py")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
