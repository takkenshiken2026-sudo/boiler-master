def html_escape(s: str) -> str:
    return (
        s.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def _table(headers: list[str], rows: list[tuple[str, list[str]]]) -> str:
    head = "".join(f"<th>{html_escape(h)}</th>" for h in headers)
    body_rows = []
    for label, cells in rows:
        tds = "".join(f"<td>{html_escape(c)}</td>" for c in cells)
        body_rows.append(f"<tr><th>{html_escape(label)}</th>{tds}</tr>")
    tbody = "".join(body_rows)
    return (
        f'<table class="{COMPARISON_TABLE_CLASS}"><thead><tr>{head}</tr></thead>'
        f"<tbody>{tbody}</tbody></table>"
    )


