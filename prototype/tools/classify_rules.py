def classify_type(code: str, name: str, category: str, scope: str, is_bucket: int):
    if is_bucket:
        return ("", "", "バケット(その他の…)")
    if scope == "overseas":
        return ("海外", "high", "海外資格(国内分類対象外)")

    # 国家（個別名）
    if name in NATIONAL_EXACT:
        return ("国家", "high", "国家資格(法律に基づく)")
    # 国家（複合条件）
    combo = _national_combo(name)
    if combo:
        return ("国家", "high", combo)
    # 国家（部分一致）
    for kw, reason in NATIONAL_SUBSTR:
        if kw in name:
            return ("国家", "high", reason)
    # 公的
    for kw, reason in PUBLIC_SUBSTR:
        if kw in name:
            return ("公的", "medium", reason)
    # 民間
    for kw, reason in PRIVATE_SUBSTR:
        if kw in name:
            return ("民間", "medium", reason)
    # 不明
    return ("要確認", "low", "未判定(一次情報での確認待ち)")
